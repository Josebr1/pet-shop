    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        UsuarioDAO ud = new UsuarioDAO();
        
        try {
            if(ud.verLogin(login.getText(), senha.getText())){
                if(ud.pegaAcesso(login.getText(), senha.getText()) == 4){
                    mg = new menuGerente();
                    mg.setVisible(true);
                    mg.recebe(ud.pegaCod(login.getText(), senha.getText()));
                }
                if(ud.pegaAcesso(login.getText(), senha.getText()) == 3){
                    mc = new menuCaixa();
                    mc.setVisible(true);
                    mc.recebe(ud.pegaCod(login.getText(), senha.getText()));
                }
                if(ud.pegaAcesso(login.getText(), senha.getText()) == 2){
                    ma = new menuAtendente();
                    ma.setVisible(true);
                    ma.recebe(ud.pegaCod(login.getText(), senha.getText()));
                }
                if(ud.pegaAcesso(login.getText(), senha.getText()) == 1){
                    me = new menuEstoque();
                    me.setVisible(true);
                    me.recebe(ud.pegaCod(login.getText(), senha.getText()));
                }
                this.dispose();
            }else {
                JOptionPane.showMessageDialog(null, "Dados Inv√°lidos");
            }
        } catch (Exception ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

